class FonctionnaireItem {
  final int id;
  final String cin;
  final String nom;
  final String prenom;
  final String email;
  final String phonenumber;
  final String gender;
  final String dateofbirth;
  FonctionnaireItem({
    required this.id,
    required this.cin,
    required this.nom,
    required this.prenom,
    required this.email,
    required this.phonenumber,
    required this.dateofbirth,
    required this.gender,
  });

}